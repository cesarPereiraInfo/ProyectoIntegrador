    private void login() throws Exception
    {
        
        String userName = txtLoginUserName.getText();
        String password = new String(txtpLoginPassword.getPassword());
        try
        {
            empleadoActual = controllerLogin.validarUsuario(userName, password);
            if (empleadoActual ==null)
            {
                JOptionPane.showMessageDialog(rootPane, "Los datos que proporcionÃ³ son incorrectos.\n\nIntente nuevamente.",
                        "Datos Incorrectos.", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                dlgLogin.setVisible(false);
                idEmpleado=empleadoActual.getId();
                us=controllerLogin.tomarRol(this.txtLoginUserName.getText());
                rol=us.getRol();
                JOptionPane.showMessageDialog(rootPane,"Bienvenido(a): "+empleadoActual.getPersona().getNombre()
                        +" "+empleadoActual.getPersona().getApePaterno()+" "+empleadoActual.getPersona().getApeMaterno()+" ðŸ˜Š"
                                + " \nRol: "+rol);
                Principal pr=new Principal(us);
                pr.setVisible(true);
                dispose();
                
                
            }
        } 
        catch (Exception e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error al validar sus datos.\n\nError:\n" + e.toString(), 
                                                    "Error de Comunicacion.", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
        
    }
    

